package com.gm.scratch1.rest;

public class Customer 
{
	String name;
	int id;
	
    public Customer (){
    }
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}